"""
Draw the letter S
"""
import turtle

turtle.shape('turtle') # defining shape

turtle.forward(50)

turtle.left(90)
turtle.forward(50)

turtle.left(90)
turtle.forward(50)

turtle.right(90)
turtle.forward(50)

turtle.right(90)
turtle.forward(50)

# Prevent window from closing so you can see the result.
turtle.mainloop()
